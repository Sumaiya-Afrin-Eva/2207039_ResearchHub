FROM PAPERS
WHERE STATUS='PUBLISHED';

-- 7
SELECT
    FIRST_NAME,
    LAST_NAME,
    EMAIL
FROM USERS
WHERE ROLE_ID=2;

-- 8
SELECT
    TITLE,
    PUBLICATION_YEAR
FROM PAPERS
ORDER BY PUBLICATION_YEAR DESC;

-- 9
SELECT
    DEPARTMENT_ID,
    COUNT(*) TOTAL_USERS
FROM USERS
GROUP BY DEPARTMENT_ID;

-- 10
SELECT
    RESEARCHER_ID,
    COUNT(*) TOTAL_PAPERS
FROM PAPERS
GROUP BY RESEARCHER_ID;

-- 11
SELECT
    TITLE,
    STATUS
FROM PAPERS
WHERE RESEARCHER_ID=2;

-- 12
SELECT
    U.FIRST_NAME,
    U.LAST_NAME,
    P.TITLE
FROM USERS U
JOIN PAPERS P
ON U.USER_ID=P.RESEARCHER_ID;

-- 13
SELECT
    P.TITLE,
    R.SCORE
FROM PAPERS P
JOIN REVIEW_ASSIGNMENTS RA
ON P.PAPER_ID=RA.PAPER_ID
JOIN REVIEWS R
ON RA.ASSIGNMENT_ID=R.ASSIGNMENT_ID;

-- 14
SELECT
    D.DEPARTMENT_NAME,
    COUNT(P.PAPER_ID) TOTAL_PAPERS
FROM DEPARTMENTS D
LEFT JOIN USERS U
ON D.DEPARTMENT_ID=U.DEPARTMENT_ID
LEFT JOIN PAPERS P
ON U.USER_ID=P.RESEARCHER_ID
GROUP BY D.DEPARTMENT_NAME;

-- 15
SELECT
    TITLE
FROM PAPERS
WHERE PAPER_ID IN
(
    SELECT PAPER_ID
    FROM REVIEW_ASSIGNMENTS
);

-- 16
SELECT
    TITLE,
    STATUS
FROM PAPERS
WHERE STATUS=
(
    SELECT STATUS
    FROM PAPERS
    WHERE PAPER_ID=1
);

-- 17
SELECT
    AVG(SCORE)
FROM REVIEWS;

-- 18
SELECT
    MAX(SCORE)
FROM REVIEWS;

-- 19
SELECT
    MIN(SCORE)
FROM REVIEWS;

-- 20
SELECT *
FROM AUDIT_LOGS
